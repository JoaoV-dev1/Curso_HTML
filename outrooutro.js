function guardarGenero(g) {
    genero = g;
    console.log("Genero guardado: " + genero);

    const trembolona = document.getElementById("trembolona");
    trembolona.innerHTML = "";
    trembolona.style.display = "none";

    trembolona.innerHTML = `
    <p>escolha seu treino:</p>
    <button onclick="treino('ABCD')">ABCD</button>
    <button onclick="treino('ABCDE')">ABCDE</button>
    <button onclick="treino('PPL')">PPL</button>
    <button onclick="treino('UL')">UL</button>
    `;
    trembolona.style.display = "block";
}